#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int		i;

	i = -1;
	while (str[++i])
		;
	while (i)
	{
		if (str[i] == (unsigned char)c)
			return ((char *)str + i);
		i--;
	}
	if (str[i] == (unsigned char)c)
		return ((char *)str + i);
	return (0);
}
